package core;

public class MathFnsDemo {
    public static void main(String[] args) {
        int a = 60;
        System.out.println("Floor:"+Math.cbrt(Math.random()));
        System.out.println("Cbrt:"+Math.cbrt(8));
        System.out.println("Add exact:"+Math.addExact(9, 56));
        System.out.println("Power:"+Math.pow(1.58, 3));
        System.out.println("Maximum of 2 is "+Math.max(56,67));
        System.out.println("Log function "+Math.log(a));
    }
}
